from Cola import Cola
puntosatencion = Cola()
transacciones = Cola()
class Empresa:
    def __init__(self, id, nombre, abreviatura):
        self.id = id
        self.nombre = nombre
        self.abreviatura = abreviatura
        self.siguiente = None
    def agregarPuntos(puntoatencion):
        puntosatencion.insertar(puntoatencion)
    def agregarTransaccion(transaccion):
        transacciones.insertar(transaccion)